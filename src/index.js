function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Co.</h1>;
    </header>
  );
}

function Menu() {
  return (
    <menu className="menu">
      <h2>Our menu</h2>
      {pizzaData.length > 0 ? (
        <>
          <p>
            Authentic Italian cuisine. 6 creative dishes to choosde from. All
            from our stone oven, all organic, all delicous.
          </p>

          <ul className="pizzas">
            {pizzaData.map((pizza) => (
              <Pizza pizzaObj={pizza} key={pizza.name} />
            ))}
          </ul>
        </>
      ) : (
        <p>We're working on Our Menu, Come back soon!</p>
      )}
    </menu>
  );
}

function Pizza({ pizzaObj }) {
  return (
    <li className={`pizza ${(pizzaObj.soldOut || "") && "sold-out"}`}>
      <img src={pizzaObj.photoName} alt={pizzaObj.name} />
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.soldOut ? "SOLD OUT" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const openHour = 8;
  const closeHour = 24;

  const hour = new Date().getHours();
  const isOpen = openHour <= hour && closeHour >= hour;

  //   return React.createElement("footer", null, "We're currently open!");
  return (
    <footer className="footer">
      {isOpen ? (
        <div className="order">
          <p>We're Open until {closeHour}:00. Come visut us or order online</p>

          <button className="btn">Order</button>
        </div>
      ) : (
        <p>
          You're welcome to visit between {openHour} and {closeHour}.
        </p>
      )}
    </footer>
  );
}

const root = ReactDom.createRoot(document.getElementById("root"));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
